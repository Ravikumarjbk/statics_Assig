#Q4>""" 1>Nominal level of measurement: At the nominal level, data are categorized into distinct and non-numeric categories or labels.
"""" EX: Eye colors of a group of people (blue, brown, green)
2> Ordinal Level of Measurement:
The ordinal level of measurement involves data that can be ranked or ordered. 
The categories have a meaningful order, but the intervals between values may not be equal, and the data lacks precise numerical meaning.

Example: Education levels (elementary, high school, bachelor's, master's, Ph.D.).
3> Interval Level of Measurement:
Interval data have equal intervals between values, but there is no true zero point. 
The zero value is arbitrary, and only the differences between values are meaningful. Interval data can be added, subtracted, 
and used for relative comparisons.

Example: Temperature measured in Celsius or Fahrenheit. While the differences between 10°C and 20°C are equal to the differences between 30°C and 40°C, 0°C does not represent the complete absence of temperature.
4> Ratio Level of Measurement:
The ratio level of measurement is similar to the interval level but with a true zero point. 
Ratios between values are meaningful, and all arithmetic operations (addition, subtraction, multiplication, division) 
are valid. Ratio data is the most precise level of measurement.

Example: Height, weight, age, time, or income. Ratios between these values are meaningful, and 0 height (no height) or 0 weight (no weight) represents an absence of the attribute."""
